'Las variables son espacios de memoria que usamos dentro del programa'

a = 5
b = 8
c = a + b

print(c)

nombre = 'Dani'
print(nombre)

# Concatenacion
bienvenida = "Hola " + nombre + " cómo estás?"
print(bienvenida)

# Tomamos un dato y se transforma en texto
bienvenidaF = f"Hola {nombre} cómo estás?" 
print(bienvenidaF)

# Para eliminar la declaración de la variable, borrar datos
del nombre;
print(bienvenidaF)


